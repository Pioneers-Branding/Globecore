<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>The Future of Psychiatry</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The future of psychiatry looks promising, with trends such as:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Expansion of <strong>telepsychiatry</strong> and <strong>digital mental health tools</strong><strong><br></strong></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Broader integration into <strong>primary care</strong> and <strong>community health systems</strong><strong><br></strong></li>
