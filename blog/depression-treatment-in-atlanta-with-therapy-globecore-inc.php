<h2 class="wp-block-heading">The Spectrum Of Depressive Disorders And Treatment Matched To Your Experience</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Depression is not one single condition. It includes several related disorders that share low mood and loss of interest but differ in timing, triggers, and intensity. Knowing which form of depression you are facing helps guide the most helpful care plan, especially since Mental Health Personality Disorder conditions can present with overlapping symptoms that require careful differential diagnosis. This is where a thorough, culturally informed&nbsp;<strong>psychological evaluation</strong>&nbsp;matters.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Some of the most common depressive disorders include:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li><strong>Major Depressive Disorder (MDD)</strong><br>This is what many people think of first when they hear “depression.” It involves at least two weeks of intense symptoms such as low mood, loss of interest, sleep or appetite changes, guilt, and poor concentration. Some people experience only one episode; others have several episodes across their lives.</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>Persistent Depressive Disorder (PDD)</strong><br>This is a longer-lasting, lower-level depression, with symptoms present for at least two years. It can feel like a constant “gray cloud” rather than a severe storm. People sometimes think this is just their personality when, in fact, it is treatable.</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>Seasonal Affective Disorder (SAD)</strong><br>Seasonal patterns can lead to SAD, where symptoms rise in fall or winter and ease in spring. Shorter daylight hours and weather changes can affect mood, energy, and sleep.</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>Perinatal Depression (Prenatal And Postpartum)</strong><br>Pregnancy and the months after birth can bring prenatal and postpartum depression. These go far beyond the “baby blues” and can affect bonding, self-worth, and safety for both parent and child.</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li><strong>Premenstrual Dysphoric Disorder (PMDD)</strong><br>PMDD is a severe form of premenstrual symptoms that strongly affect mood and daily life in the days before a period. Irritability, anger, sadness, and emotional swings can feel intense and disruptive.</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Depressive symptoms can also be caused or worsened by&nbsp;<strong>medical conditions</strong>&nbsp;such as thyroid problems, chronic pain, or Parkinson’s disease, or by substance use and certain medications. Sorting out these connections is not something you have to do alone. At GlobeCoRe, Ph.D. and Masters-level clinicians listen carefully to your story, pay attention to how symptoms show up in your body and culture, and review your health history. This careful assessment supports a diagnosis that truly matches your experience, which then guides a treatment plan that fits you rather than a one-size-fits-all model.</p>
<!-- /wp:paragraph -->

