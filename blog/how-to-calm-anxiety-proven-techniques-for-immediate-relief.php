<h2 class="wp-block-heading">When to Seek Professional Help for Anxiety</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>While self-help strategies can be incredibly effective, there are times when professional care is necessary. You should reach out to a mental health provider if:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Your anxiety is constant and disrupting your daily life</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>You are avoiding work, school, social situations, or relationships because of anxiety</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>You are having panic attacks regularly</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>You feel like you cannot relax no matter what you try</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>You are using alcohol, drugs, or other substances to cope with anxiety</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>You are having thoughts of harming yourself</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

