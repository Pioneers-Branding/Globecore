<p>Depression is not just a difficult season. For many individuals, it is a chronic, life-altering condition that affects work performance, relationships, decision-making, and even the ability to complete basic daily tasks. A question that comes up frequently at <strong>GlobeCoRe in Atlanta, GA,</strong> is:</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>Can you get disability for depression?</strong></p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The answer is yes, but qualifying requires meeting specific medical and functional standards.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>This guide walks you through eligibility, documentation requirements, the application process, and what Atlanta residents need to know before applying.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Understanding Depression as a Recognized Disability</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Clinical depression is recognized as a legitimate medical condition by the <a href="https://en.wikipedia.org/wiki/Social_Security_Disability_Insurance" rel="nofollow" target="_blank">Social Security Administration (SSA)</a> (SSA). It appears under Section 12.04 of the SSA’s “Blue Book,” which outlines impairments that may qualify for disability benefits.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Depression becomes potentially disabling when it significantly limits your ability to function in a work setting.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>Symptoms That May Support a Disability Claim</strong></h3>
<!-- /wp:heading -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Persistent low mood lasting most of the day<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Loss of interest in previously enjoyed activities<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Difficulty concentrating or making decisions<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Chronic fatigue<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Sleep disturbances (insomnia or hypersomnia)<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Appetite or weight changes<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Feelings of hopelessness or worthlessness<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Recurrent thoughts of death or suicide<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>It’s not about having symptoms occasionally; it’s about how severely they interfere with sustained employment.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Types of Disability Benefits Available</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>There are two main federal disability programs:</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>1. Social Security Disability Insurance (SSDI)</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Designed for individuals who:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Have worked and paid Social Security taxes<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Earned sufficient work credits<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>2. Supplemental Security Income (SSI)</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Designed for individuals who:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Have limited income and resources<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>May not have an extensive work history<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Both programs use the same medical evaluation criteria.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>How the SSA Evaluates Depression Claims</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>To qualify under listing 12.04, you must meet both medical documentation requirements and functional limitation standards.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>A. Medical Criteria</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>You must provide documentation of at least five of the following:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li><a href="https://globecoreinc.com/specialties/depression-anxiety/">Depressed mood</a><br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Diminished interest in activities<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Appetite disturbance<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Sleep disturbance<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Observable psychomotor changes<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Decreased energy<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Feelings of guilt or worthlessness<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Difficulty concentrating<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Thoughts of death or suicide<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Medical evidence must come from licensed professionals and include consistent treatment records.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>B. Functional Limitations</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The SSA evaluates how depression affects:</p>
<!-- /wp:paragraph -->

<!-- wp:list {"ordered":true} -->
<ol class="wp-block-list"><!-- wp:list-item -->
<li>Understanding and remembering information<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Interacting with others<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Concentrating and completing tasks<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Adapting or managing oneself<br></li>
<!-- /wp:list-item --></ol>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>To qualify, you must show:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Marked limitation in at least two areas<br>OR<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Extreme limitation in one area<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>This is where thorough psychiatric documentation becomes critical.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>What If You Don’t Meet the Blue Book Criteria?</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Many people still qualify through a Residual Functional Capacity (RFC) evaluation.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>An RFC determines whether you can sustain full-time employment despite your symptoms. It examines:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Ability to maintain attendance<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Tolerance for workplace stress<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Focus and productivity<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Interpersonal functioning<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Even if your depression doesn’t exactly match listing 12.04, severe work-related limitations may still qualify you.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>How Long Must Depression Last?</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The SSA requires that:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>The condition has lasted at least 12 months<br>OR<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Is expected to last 12 months<br>OR<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Is expected to result in death<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Short-term depressive episodes typically do not qualify.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>The Importance of Ongoing Treatment</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>A common reason claims are denied is a lack of consistent treatment.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Strong documentation includes:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li><a href="https://globecoreinc.com/services/psychological-evaluation/">Psychiatric evaluations</a><br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Therapy notes<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Medication history<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Hospital records (if applicable)<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Functional assessments<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>At GlobeCoRe in Atlanta, structured evaluations and treatment planning help create accurate documentation when needed.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Can You Work While Applying?</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Yes, but income limits apply.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>If you earn above the Substantial Gainful Activity (SGA) threshold, your claim may be denied.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Part-time work may be allowed, depending on earnings.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Why Many Claims Are Initially Denied</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>It’s common for first-time applications to be denied. Reasons include:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Insufficient medical evidence<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Gaps in treatment<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Incomplete forms<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Failure to follow prescribed treatment<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Earning above SGA limits<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Appeals often result in approvals when additional documentation is provided.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
