<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Complete work history forms<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Attend consultative exams (if requested)<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Await decision<br></li>
<!-- /wp:list-item --></ol>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Processing time: typically 3-6 months. Appeals may take additional time.</p>
<!-- /wp:paragraph -->

