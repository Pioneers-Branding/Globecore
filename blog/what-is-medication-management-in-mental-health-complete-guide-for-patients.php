<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Prescribing medications<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Monitoring side effects<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Adjusting doses<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Ensuring treatment safety<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Providing education and long-term management<br></li>
<!-- /wp:list-item --></ul>
