                        <h2 id="covered-services">What Mental Health Services Are Covered by TRICARE?</h2>
                        <p>TRICARE provides robust coverage for medically necessary mental health treatments. These covered services include:</p>

                        <h3>Outpatient Psychotherapy</h3>
                        <p>This includes individual talk therapy, group therapy sessions, and family counseling. These services are covered when provided by licensed, TRICARE-authorized professionals, including clinical psychologists, licensed clinical social workers (LCSWs), and marriage and family therapists (LMFTs).</p>

                        <h3>Transcranial Magnetic Stimulation (TMS)</h3>
                        <p>For individuals struggling with major depressive disorder who have not found relief from traditional antidepressants, TRICARE covers FDA-cleared, non-invasive neuromodulation. Learn more about accessing <a href="/services/tricare-tms-therapy.php">TRICARE-approved TMS therapy</a> as a highly effective option for treatment-resistant depression.</p>

                        <h3>Psychological Testing and Evaluations</h3>
                        <p>Standardized diagnostic testing for learning disabilities, ADHD, and complex diagnostic profiles is covered when deemed medically necessary. Typically, prior authorization from your regional contractor (such as Humana Military or Health Net Federal Services) is required for psychological testing.</p>

