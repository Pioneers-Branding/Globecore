                        <h2 id="faqs">Frequently Asked Questions</h2>

                        <h3>Can I see an out-of-network therapist with TRICARE Prime?</h3>
                        <p>Yes, but it will be processed under the **Point of Service (POS)** option. Under POS, you will face a yearly deductible ($300 for individuals, $600 for families) and a 50% cost-share of the TRICARE-allowable charge. To avoid these high costs, Prime enrollees should prioritize in-network therapists.</p>

                        <h3>Are online therapy and telehealth sessions covered?</h3>
                        <p>Yes. TRICARE offers comprehensive coverage for telehealth counseling sessions provided by licensed, authorized providers. The same referral and authorization rules apply as for in-person treatment.</p>

                        <h3>Do I need a referral for psychiatrist appointments or medication management?</h3>
                        <p>For outpatient psychiatric consultations and medication management, family members and retirees do not need a referral, provided they visit an in-network psychiatrist. Prior authorization may be required for specific psychiatric prescriptions or highly intensive treatment structures.</p>

