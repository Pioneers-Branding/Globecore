<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Right Timing</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Right Patient</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Minimum Side Effects</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Maximum Benefit</li>
<!-- /wp:list-item --></ul>
