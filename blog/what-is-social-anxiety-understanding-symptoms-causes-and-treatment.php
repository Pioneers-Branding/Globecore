<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Job interviews<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Many professionals in Atlanta struggle specifically with performance anxiety that impacts career advancement.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>What Causes Social Anxiety?</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>There is no single cause. Social anxiety typically develops from a combination of biological, psychological, and environmental factors.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>1. Genetics</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Individuals with family members who have anxiety disorders are more likely to develop social anxiety.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>2. Brain Chemistry</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Imbalances in neurotransmitters such as serotonin may contribute to heightened fear responses.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>3. Negative Experiences</strong></h3>
<!-- /wp:heading -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Bullying<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Public humiliation<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Harsh criticism<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Trauma<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Overly controlling parenting<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Repeated negative social experiences can condition the brain to associate social interaction with danger.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>4. Temperament</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Children who are behaviorally inhibited or highly sensitive may be more prone to developing social anxiety.</p>
<!-- /wp:paragraph -->

