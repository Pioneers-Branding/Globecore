<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>A mental health professional conducts a comprehensive evaluation, including:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Symptom history<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Duration and severity<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Functional impairment<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Rule-out of medical causes<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Screening for co-occurring disorders<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>At GlobeCoRe, assessments focus on understanding both emotional and functional impact to develop a tailored treatment plan.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading"><strong>Evidence-Based Treatments for Social Anxiety</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>The encouraging news: social anxiety is highly treatable.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>1. Cognitive Behavioral Therapy (CBT)</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>CBT is considered the gold standard treatment.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>It helps individuals:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Identify distorted thoughts<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Challenge negative self-beliefs<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Reduce catastrophic thinking<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Develop coping strategies<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>For example, replacing “Everyone will think I’m incompetent” with balanced, realistic thoughts.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>2. Exposure Therapy</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Avoidance strengthens anxiety.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Gradual exposure helps retrain the brain by:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Facing feared situations in small steps<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Reducing sensitivity over time<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Building confidence through repetition<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading"><strong>3. Medication</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Selective serotonin reuptake inhibitors (SSRIs) and serotonin-norepinephrine reuptake inhibitors (SNRIs) are commonly prescribed.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Medication may:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Reduce baseline anxiety<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Improve participation in therapy<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Lower physical symptoms<br></li>
<!-- /wp:list-item --></ul>
