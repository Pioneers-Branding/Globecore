<h2 class="wp-block-heading"><strong>When Does Shyness Become Social Anxiety Disorder?</strong></h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Consider professional evaluation if:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Fear persists for six months or longer<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>You actively avoid social situations<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Anxiety causes significant distress<br></li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Symptoms interfere with work or relationships<br></li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>If fear controls your decisions rather than your values or goals, it may be time for support.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
