<!-- wp:heading -->
<h2 class="wp-block-heading">Treatment Options for ADHD</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>ADHD is a manageable condition. With the right support, people with ADHD can thrive. Treatment typically includes a combination of the following:</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Medication</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Stimulant medications like Adderall and Ritalin are the most commonly prescribed and have strong evidence behind them. Non-stimulant options like Strattera or Wellbutrin are also available. A psychiatrist will evaluate the right fit for your needs and history.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Behavioral Therapy</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Cognitive Behavioral Therapy (CBT) helps people with ADHD build coping skills, manage time better, and improve emotional regulation. It is especially effective when combined with medication.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":3} -->
<h3 class="wp-block-heading">Lifestyle Changes</h3>
<!-- /wp:heading -->

<!-- wp:list -->
<ul class="wp-block-list"><!-- wp:list-item -->
<li>Regular exercise (shown to improve focus and reduce impulsivity)</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Structured daily routines</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Sleep hygiene: poor sleep makes ADHD symptoms worse</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Mindfulness and meditation</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Limiting distractions in the work or study environment</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:heading -->
<h2 class="wp-block-heading">When to Seek Professional Help for ADHD</h2>
<!-- /wp:heading -->

