<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Inpatient mental health treatment</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Substance use disorder treatment</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Coverage depends on the type of TRICARE plan and whether the provider is authorized under the TRICARE network.</p>
